import turtle
import math


def setUpMaze(level, stepSize):
    width = len(level[0])*stepSize
    height = len(level)*stepSize
    turtle.setup(width+80, height+80)
    walls = turtle.Turtle()
    walls.shape('square')
    walls.speed(100)
    walls.penup()
    wall = [[]]
    print(wall)
    print('hh')
    target=[]
    agent = []

    for i in range(len(level)):
        for j in range(len(level[0])):
            x = -width/2 + j*stepSize
            y = height/2 - i*stepSize
            chr = level[i][j]
            if chr == 'X':
                walls.goto(x,y)
                walls.stamp()
                wall.append([x,y])
            if chr == 'T':
                target = [x, y]
            if chr == 'A':
                agent = [x, y]

    return wall, agent, target





